#include <unistd.h>

int	ft_str_is_printable(char *str)
{
	int	i;
	int	a;

	i = 0;
	a = 1;
	if (str)
	{
		while (str[i])
		{
			if (!(str[i] >= 32 && str[i] <= 126))
			{	
				a = 0;
				break ;
			}
			i++;
		}
	}
	return (a);
}
